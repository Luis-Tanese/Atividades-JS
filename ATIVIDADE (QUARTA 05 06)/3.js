// 3) Programar a conversão de uma temperatura digitada pelo usuário em graus Celsius para Fahrenheit. Mostrar o resultado na tela. 
var Celsius

Celsius = Number(prompt("Digite o valor em célsius que você deseja converter para fahrenheit: "))

resultado = (Celsius * 1.8) + 32

alert("O valor de " + Celsius + "° célsius em fahrenheit é: " + resultado)